@app.route("/", methods=["GET", "POST"])
def currency_converter():
    if request.method == "POST":
        amount = request.form.get("amount")
        from_currency = request.form.get("from_currency")
        to_currency = request.form.get("to_currency")
        result = get_exchange_rate(amount, from_currency, to_currency)
    else:
        amount = 1
        from_currency = "USD"
        to_currency = "GBP"
        result = "Getting exchange rate"

    from_currency_code = country_List[from_currency].lower()
    to_currency_code = country_List[to_currency].lower()

    return render_template(
        "index.html",
        currency_list=country_List,
        from_currency_code=from_currency_code,
        to_currency_code=to_currency_code,
        result=result,
    )

def get_exchange_rate(amount, from_currency, to_currency):
    try:
        response = requests.get(f"https://v6.exchangerate-api.com/v6/e82f0e573e6380e3b6ad80bf/latest/{from_currency}")
        result = response.json()
        exchange_rate = result["conversion_rates"][to_currency]
        total_exchange_rate = round(float(amount) * exchange_rate, 2)
        return f"{amount} {from_currency} = {total_exchange_rate} {to_currency}"
    except Exception as e:
        return "Something went wrong"

